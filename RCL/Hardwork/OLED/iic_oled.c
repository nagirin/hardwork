static uint32_t i2c_oled_write(uint8_t ch, uint32_t addr, const void *buf, uint32_t len)
{
    char *p;
		
		p = (char *)buf;
    uint32_t byte_remain = len;
    uint32_t byte_write = 0;
    uint32_t addr_offset = 0;

    do
    {
        byte_write = _write_bytes(ch, addr + addr_offset, p + addr_offset, byte_remain);

        byte_remain -= byte_write;
        addr_offset += byte_write;

        if (byte_write == 0)
        {
            break;
        }
    }
    while (byte_remain > 0);
    return (len - byte_remain);
}


static void i2c_oled_write_cmd(uint8_t cmd)		//写命令
{
	i2c_oled_write(I2C_OLED_1, 0x00, &cmd, sizeof(cmd)); 
}

static void i2c_oled_write_data(uint8_t data) 		//写数据
{
	i2c_oled_write(I2C_OLED_1, 0x40, &data, sizeof(data)); 
}

/**
  * @brief  OLED_SetPos，设置光标
  */
void i2c_oled_setPos(uint8_t x, uint8_t y) //设置起始点坐标
{ 
	i2c_oled_write_cmd(0xb0+y);
	i2c_oled_write_cmd(((x&0xf0)>>4)|0x10);
	i2c_oled_write_cmd((x&0x0f)|0x01);
}

 /**
  * @brief  OLED_Fill，填充整个屏幕
  */
void i2c_oled_fill(uint8_t fill_Data)//全屏填充
{
	uint8_t m,n;
	for(m=0;m<8;m++)
	{
		i2c_oled_write_cmd(0xb0+m);		//page0-page1
		i2c_oled_write_cmd(0x00);		//low column start address
		i2c_oled_write_cmd(0x10);		//high column start address
		for(n=0;n<128;n++)
			{
				i2c_oled_write_data(fill_Data);
			}
	}
}

 /**
  * @brief  OLED_CLS，清屏
  */
void i2c_oled_cls(void)	//清屏
{
	i2c_oled_fill(0x00);
}


 /**
  * @brief  OLED_ON，将OLED从休眠中唤醒
  */
void i2c_oled_wakeup(void)
{
	i2c_oled_write_cmd(0X8D);  //设置电荷泵
	i2c_oled_write_cmd(0X14);  //开启电荷泵
	i2c_oled_write_cmd(0XAF);  //OLED唤醒
}


 /**
  * @brief  OLED_OFF，让OLED休眠 -- 休眠模式下,OLED功耗不到10uA
  */
void i2c_oled_off(void)
{
	i2c_oled_write_cmd(0X8D);  //设置电荷泵
	i2c_oled_write_cmd(0X10);  //关闭电荷泵
	i2c_oled_write_cmd(0XAE);  //OLED休眠
}


